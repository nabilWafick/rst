                                card: card,
                                confirmToDelete: CardsCRUDFunctions.delete,
                              ),
                            );
                          },
                        ),
                      ],
                    ),
                  ),
                  Container(
                    alignment: Alignment.centerLeft,
                    width: 300.0,
                    height: 30.0,
                    child: RSTText(
                      text: FunctionsController.truncateText(
                        text: card.label,
                        maxLength: 35,
