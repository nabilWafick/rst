class CardsPageBody extends StatefulHookConsumerWidget {
  const CardsPageBody({super.key});

  @override
  ConsumerState<ConsumerStatefulWidget> createState() => _CardsPageBodyState();
}

class _CardsPageBodyState extends ConsumerState<CardsPageBody> {
  @override
  void initState() {
    super.initState();
    initializeDateFormatting('fr');
  }

  @override
